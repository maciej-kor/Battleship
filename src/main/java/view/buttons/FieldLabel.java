package view.buttons;

import javax.swing.*;

public class FieldLabel extends JLabel {

    int x, y;

    public int getxX() {
        return x;
    }

    public void setxX(int x) {
        this.x = x;
    }

    public int getyY() {
        return y;
    }

    public void setyY(int y) {
        this.y = y;
    }
}
