package model;

public class Field {

    private int x;
    private int y;
    private FieldState fieldState;

    public int getXx() {
        return x;
    }

    public void setXx(int x) {
        this.x = x;
    }

    public int getYy() {
        return y;
    }

    public void setYy(int y) {
        this.y = y;
    }

    public FieldState getFieldState() {
        return fieldState;
    }

    public void setFieldState(FieldState fieldState) {
        this.fieldState = fieldState;
    }
}

